SELECT DISTINCT TRUNC(cast("O04T14"."REGDATE" AS DATE)) AS "Reg_Date",
                "O04T14"."ORDNO" AS "Order_Number",
                "O08T12"."PARTNO" AS "Article_Number",
                "O08T12"."REQQUANT" AS "Order_Quantity",
                "O04T14"."ORDSTAT" AS "Order_Status",
                "A_L62T1_O420"."PARTREV" AS "Supplier",
                "O04T14"."ORDTYPE" AS "Order_Type",
                "O04T90"."ORDPLTYP" AS "Pallet_Type"
FROM "A_L62T1_O420"
INNER JOIN "O42T2" "O42T2" ON "A_L62T1_O420"."SHORTL62" = "O42T2"."SHORTL62"
INNER JOIN "O08T12" ON "O08T12"."SHORTO08" = "O42T2"."SHORTO08"
INNER JOIN "O04T14" ON "O08T12"."DIVCODE" = "O04T14"."DIVCODE"
AND "O08T12"."ORDNO" = "O04T14"."ORDNO"
AND "O08T12"."SHORTO04" = "O04T14"."SHORTO04"
INNER JOIN "O04T90" "O04T90" ON "O04T90"."SHORTO04" = "O04T14"."SHORTO04"
WHERE NOT ("O04T14"."ORDSTAT" BETWEEN 0 AND 0)
  AND NOT ("O04T14"."ORDSTAT" BETWEEN 6 AND 6)
  AND NOT ("O04T14"."ORDSTAT" BETWEEN 9 AND 9)
  AND NOT ("O04T14"."ORDSTAT" BETWEEN 7 AND 7)
  AND "O04T14"."ORDTYPE" BETWEEN 1 AND 1